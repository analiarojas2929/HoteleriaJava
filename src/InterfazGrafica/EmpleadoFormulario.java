    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        int rut;
        String dv;
        String nombre;
        String apellido;
        String direccion;
        String telefono;
        String genero;
        String tipoEmpleado;
        Date fechaNacimiento;
        String correo;

        //rescatar y validar datos
        rut = Integer.parseInt(txtRut.getText());
        dv = cboDv.getSelectedItem().toString();
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        direccion = txtDireccion.getText();
        telefono = txtTelefono.getText();
        genero = cboGenero.getSelectedItem().toString();
        tipoEmpleado = cboTipoEmpleado.getSelectedItem().toString();
        fechaNacimiento = dtaFechaNacimiento.getDate();
        correo = txtCorreo.getText();

        //crear empleado
        Empleado empleado = new Empleado(rut, dv, nombre, apellido, direccion, telefono, genero, tipoEmpleado, fechaNacimiento, correo);

        //agregar empleado
        Registro r = new Registro();
        if (r.agregarEmpleado(empleado)) {
            JOptionPane.showMessageDialog(this, "Se agrego Empleado " + nombre, "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se agrego Empleado " + nombre, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnagregarActionPerformed

